// 의료법 안전 가드레일 + 답변 룰 + 카테고리 분류.
// 어드민이 편집할 수 없는 코드 단 고정 영역.
//
// 톤은 어드민이 clinic_settings.settings.tone으로 선택 ("warm" | "formal").
// 의료 안전 컨텐츠는 두 톤 모두 동일하며 어미·표현만 다름.

export function safetyRules({ clinicName, bookingUrl, tone = "warm" }) {
  if (tone === "formal") {
    return formalRules({ clinicName, bookingUrl });
  }
  return warmRules({ clinicName, bookingUrl });
}

