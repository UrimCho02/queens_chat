// 진료과 무관 안전 룰 + 공통 규칙 + 톤 가이드 + 기본 카테고리.
// 어드민이 편집할 수 없는 코드 단 고정 영역.
//
// specialty 별 추가 룰은 ./{obgyn,internal,pediatric}.js 가 별도로 export.
// index.js 가 두 모듈을 합성해 최종 시스템 프롬프트 섹션을 만든다.

export function commonBlock({ clinicName, bookingUrl, tone = "warm" }) {
  if (tone === "formal") return formalBlock({ clinicName, bookingUrl });
  return warmBlock({ clinicName, bookingUrl });
}

